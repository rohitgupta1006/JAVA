public class q30 {
    public static void main(String[] args) {
        int n=5;
        int row=1;
        while(row<=n)
        {
            int col=5;
            while(col>=1)
            {
                System.out.print(col+" ");
                col--;
            }
            System.out.println();
            row++;
        }
    }
    
}
    

